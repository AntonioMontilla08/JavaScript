//Sintaxis
//Se declara el indice o iniciador del loop
//Se usa la palabra reservada do y en los corchetes la accion a ejecutar
//Al final del "do", antes del corchete de cierre, se pone el incremento
//Después del corchete de cierre la palabra reservada While y entre parentesis la condición a evaluar

let i = 0;

do{
    console.log(i);
    i++
} while (i < 10);